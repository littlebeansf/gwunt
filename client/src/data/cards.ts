// Build starter decks per faction (22 cards)
export function buildStarterDeck(faction: string): CardDef[] {
  const factionCards = ALL_CARDS.filter(c => c.faction === faction);
  const neutralCards = ALL_CARDS.filter(c => c.faction === 'neutral');
  
  // Pick 14 faction units, 3 faction specials, 3 neutral specials, 2 neutral units
  const factionUnits = factionCards.filter(c => c.type === 'unit').slice(0, 14);
  const factionSpecials = factionCards.filter(c => c.type === 'special' || c.type === 'weather').slice(0, 3);
  const neutralSpecials = neutralCards.filter(c => c.type === 'special' || c.type === 'weather').slice(0, 3);
  const neutralUnits = neutralCards.filter(c => c.type === 'unit').slice(0, 2);
  
  return [...factionUnits, ...factionSpecials, ...neutralSpecials, ...neutralUnits];
}
